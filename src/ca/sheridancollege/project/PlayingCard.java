package ca.sheridancollege.project;

/**
 *
 * @author 991764920
 */
public class PlayingCard extends Card {
    private int rank;
    private String suit;

    public PlayingCard(int rank, String suit) {
        this.rank = rank;
        this.suit = suit;
    }

    public int getRank() {
        return rank;
    }

    public String getSuit() {
        return suit;
    }

    @Override
    public String toString() {
        return rank + " of " + suit;
    }
}
